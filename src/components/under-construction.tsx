function AnimatedDots() {
  const [dots, setDots] = useState('')
  
  useEffect(() => {
    const interval = setInterval(() => {
      setDots(prev => prev.length >= 3 ? '' : prev + '.')
    }, 800)
    return () => clearInterval(interval)
  }, [])
  
  return <span className="text-[#a7d26d]">{dots}</span>
}

